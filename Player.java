
/**
 * The player class for the Tick Tac Toe Game
 *
 * @author Aditya Goyal
 */
public class Player
{
    /**
     * === Attributes ===
     * name: the name of the player
     */
    String name;
    Player()
    {
        this.name = "";
    }
    Player(String n)
    {
        this.name = n;
    }
}